function initChecklist() {
    const checkboxes = document.querySelectorAll('.checklist-checkbox');
    
    // Load initial states
    let savedState = {};
    try {
        const stored = localStorage.getItem(CHECKLIST_KEY);
        if (stored) {
            savedState = JSON.parse(stored);
        }
    } catch (e) {
        console.error("Gagal memuat status checklist berkas", e);
    }

    checkboxes.forEach(cb => {
        const id = cb.getAttribute('data-id');
        if (savedState[id]) {
            cb.checked = true;
        }

        cb.addEventListener('change', () => {
            savedState[id] = cb.checked;
            try {
                localStorage.setItem(CHECKLIST_KEY, JSON.stringify(savedState));
            } catch (e) {
                console.error("Gagal menyimpan status checklist berkas", e);
            }
            updateChecklistProgress();
        });
    });

    updateChecklistProgress();
}

function updateChecklistProgress() {
    const checkboxes = document.querySelectorAll('.checklist-checkbox');
    if (checkboxes.length === 0) return;

    let checkedCount = 0;
    checkboxes.forEach(cb => {
        if (cb.checked) checkedCount++;
    });

    const percentage = Math.round((checkedCount / checkboxes.length) * 100);
    
    document.getElementById('checklist-percentage').textContent = `${percentage}%`;
    document.getElementById('checklist-progress').style.width = `${percentage}%`;
}

