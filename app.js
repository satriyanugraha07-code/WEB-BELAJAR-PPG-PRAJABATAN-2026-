// Timer management
function startQuizTimer() {
    stopQuizTimer();
    updateTimerDisplay();

    currentQuizState.timer = setInterval(() => {
        currentQuizState.timeLeft--;
        updateTimerDisplay();

        if (currentQuizState.timeLeft <= 0) {
            stopQuizTimer();
            alert("Waktu ujian telah habis! Lembar jawaban Anda akan langsung dikirim.");
            submitQuiz();
        }
    }, 1000);
}

function stopQuizTimer() {
    if (currentQuizState.timer) {
        clearInterval(currentQuizState.timer);
        currentQuizState.timer = null;
    }
}

function updateTimerDisplay() {
    const minutes = Math.floor(currentQuizState.timeLeft / 60);
    const seconds = currentQuizState.timeLeft % 60;
    
    const formattedMin = minutes.toString().padStart(2, '0');
    const formattedSec = seconds.toString().padStart(2, '0');
    
    const display = document.getElementById('timer-display');
    if (display) display.textContent = `${formattedMin}:${formattedSec}`;

    const timerBox = document.getElementById('quiz-timer-container');
    if (timerBox) {
        // Red alert styling for remaining time under 2 minutes
        if (currentQuizState.timeLeft < 120) {
            timerBox.style.color = 'var(--danger)';
            timerBox.style.backgroundColor = 'var(--danger-light)';
        } else {
            timerBox.style.color = 'var(--accent)';
            timerBox.style.backgroundColor = 'rgba(236, 72, 153, 0.08)';
        }
    }
}

// Submission and Results
function submitQuiz() {
    stopQuizTimer();
    document.body.classList.remove('quiz-mode-active');

    if (document.fullscreenElement) {
        document.exitFullscreen().catch(err => {
            console.log("Gagal keluar mode fullscreen:", err);
        });
    }

    let correctCount = 0;
    let wrongCount = 0;
    const totalQ = currentQuizState.questions.length;

    currentQuizState.questions.forEach((q, idx) => {
        const userAns = currentQuizState.answers[idx];
        let isCorrect = false;

        if (q.type === 'multi') {
            // Both userAns and correct are arrays. Check if same elements (order doesn't matter)
            if (userAns && Array.isArray(userAns)) {
                const sortedUser = [...userAns].sort();
                const sortedCorrect = [...q.correct].sort();
                isCorrect = JSON.stringify(sortedUser) === JSON.stringify(sortedCorrect);
            }
        } else if (q.type === 'table') {
            // Check if every boolean array item matches
            if (userAns && Array.isArray(userAns)) {
                isCorrect = userAns.every((v, i) => v === q.correct[i]);
            }
        } else {
            // Single MC
            isCorrect = userAns === q.correct;
        }

        if (isCorrect) {
            correctCount++;
        } else {
            wrongCount++;
        }
    });

    const finalScore = Math.round((correctCount / totalQ) * 100);

    // Save history score locally
    try {
        const quizHistory = JSON.parse(localStorage.getItem('ayo_ppg_quiz_history') || '[]');
        quizHistory.push({
            category: currentQuizState.category,
            score: finalScore,
            correct: correctCount,
            total: totalQ,
            date: new Date().toISOString()
        });
        localStorage.setItem('ayo_ppg_quiz_history', JSON.stringify(quizHistory));
    } catch(e) {
        console.error("Gagal menyimpan riwayat skor kuis", e);
    }



    // Render results view
    document.getElementById('res-score').textContent = finalScore;
    document.getElementById('res-correct').textContent = correctCount;
    document.getElementById('res-wrong').textContent = wrongCount;

    // Render Detailed Review list
    renderDetailedReview();

    // Toggle views
    document.getElementById('quiz-container').classList.add('hidden');
    document.getElementById('quiz-results').classList.remove('hidden');

    window.scrollTo({
        top: document.getElementById('kuis-section').offsetTop - 90,
        behavior: 'smooth'
    });
}

function renderDetailedReview() {
    const reviewList = document.getElementById('review-list');
    if (!reviewList) return;
    
    reviewList.innerHTML = '';
    const alphaMarkers = ['A', 'B', 'C', 'D', 'E'];

    currentQuizState.questions.forEach((q, idx) => {
        const userAns = currentQuizState.answers[idx];
        let isCorrect = false;

        // Check correctness based on type
        if (q.type === 'multi') {
            if (userAns && Array.isArray(userAns)) {
                const sortedUser = [...userAns].sort();
                const sortedCorrect = [...q.correct].sort();
                isCorrect = JSON.stringify(sortedUser) === JSON.stringify(sortedCorrect);
            }
        } else if (q.type === 'table') {
            if (userAns && Array.isArray(userAns)) {
                isCorrect = userAns.every((v, i) => v === q.correct[i]);
            }
        } else {
            isCorrect = userAns === q.correct;
        }

        const reviewItem = document.createElement('div');
        reviewItem.className = 'review-item';

        // Header
        const header = document.createElement('div');
        header.className = 'rev-opt ' + (isCorrect ? 'correct' : 'wrong');
        header.style.padding = '12px';
        header.style.fontWeight = 'bold';
        header.innerHTML = `
            <span>Pertanyaan ${idx + 1}</span>
            <span class="rev-badge ${isCorrect ? 'correct' : 'wrong'}">
                ${isCorrect ? 'BENAR' : 'SALAH / KOSONG'}
            </span>
        `;
        reviewItem.appendChild(header);

        // Question text
        const qText = document.createElement('div');
        qText.className = 'review-q-text';
        qText.style.marginTop = '12px';
        qText.innerHTML = q.question;
        reviewItem.appendChild(qText);

        // Render Answer comparison details
        const compareBlock = document.createElement('div');
        compareBlock.className = 'review-explanation';
        compareBlock.style.marginTop = '12px';
        compareBlock.style.backgroundColor = 'var(--bg-main)';
        compareBlock.style.borderLeft = '4px solid var(--text-muted)';
        
        let detailsHtml = '';
        if (q.type === 'multi') {
            const hasAns = userAns && Array.isArray(userAns) && userAns.length > 0;
            const userStr = hasAns ? userAns.map(i => alphaMarkers[i]).join(', ') : "Tidak Dijawab";
            const correctStr = q.correct.map(i => alphaMarkers[i]).join(', ');
            detailsHtml = `
                <p><strong>Jawaban Anda:</strong> [${userStr}]</p>
                <p><strong>Jawaban Kunci:</strong> [${correctStr}]</p>
            `;
        } else if (q.type === 'table') {
            let userRows = '';
            q.statements.forEach((stmt, sIdx) => {
                const isSelected = userAns && Array.isArray(userAns) && userAns[sIdx] !== null && userAns[sIdx] !== undefined;
                const uVal = isSelected ? (userAns[sIdx] === true ? 'Benar' : 'Salah') : 'Tidak Dijawab';
                const cVal = q.correct[sIdx] === true ? 'Benar' : 'Salah';
                const rowClass = (isSelected && userAns[sIdx] === q.correct[sIdx]) ? 'text-success' : 'text-danger';
                
                userRows += `
                    <tr class="${rowClass}">
                        <td>${stmt}</td>
                        <td>${uVal}</td>
                        <td><strong>${cVal}</strong></td>
                    </tr>
                `;
            });
            detailsHtml = `
                <table class="utbk-table" style="font-size:0.8rem; width:100%; margin-top:8px;">
                    <thead><tr><th>Pernyataan</th><th>Anda</th><th>Kunci</th></tr></thead>
                    <tbody>${userRows}</tbody>
                </table>
            `;
        } else {
            const hasAns = userAns !== null && userAns !== undefined && q.options[userAns] !== undefined;
            const userStr = hasAns ? alphaMarkers[userAns] + '. ' + q.options[userAns] : "Tidak Dijawab";
            const correctStr = q.options[q.correct] !== undefined ? alphaMarkers[q.correct] + '. ' + q.options[q.correct] : "Tidak Ada Kunci";
            detailsHtml = `
                <p><strong>Jawaban Anda:</strong> ${userStr}</p>
                <p><strong>Jawaban Kunci:</strong> ${correctStr}</p>
            `;
        }
        
        compareBlock.innerHTML = detailsHtml;
        reviewItem.appendChild(compareBlock);

        // Explanation block
        const explanationBlock = document.createElement('div');
        explanationBlock.className = 'review-explanation';
        explanationBlock.style.marginTop = '8px';
        explanationBlock.innerHTML = `
            <strong>Pembahasan:</strong><br>
            <p>${q.explanation}</p>
        `;
        reviewItem.appendChild(explanationBlock);

        reviewList.appendChild(reviewItem);
    });
}

