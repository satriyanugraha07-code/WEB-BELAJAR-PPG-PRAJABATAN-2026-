function navigateToSection(sectionId) {
    if (activeSectionId === sectionId) {
        window.scrollTo({ top: 0, behavior: 'smooth' });
        return;
    }

    // Save previous section and stop quiz timer if leaving quiz section
    if (activeSectionId === 'kuis' && sectionId !== 'kuis') {
        stopQuizTimer();
        document.body.classList.remove('quiz-mode-active');
    }

    // Hide active section
    const currentSection = document.getElementById(`${activeSectionId}-section`);
    if (currentSection) {
        currentSection.classList.remove('active-section');
    }

    // Show new section
    const targetSection = document.getElementById(`${sectionId}-section`);
    if (targetSection) {
        targetSection.classList.add('active-section');
    }

    // Update active nav state (Desktop)
    const navLinks = document.querySelectorAll('.nav-link');
    navLinks.forEach(link => {
        if (link.getAttribute('data-target') === sectionId) {
            link.classList.add('active');
        } else {
            link.classList.remove('active');
        }
    });

    // Update active nav state (Mobile)
    const mobileNavLinks = document.querySelectorAll('.mobile-nav-link');
    mobileNavLinks.forEach(link => {
        if (link.getAttribute('data-target') === sectionId) {
            link.classList.add('active');
        } else {
            link.classList.remove('active');
        }
    });

    activeSectionId = sectionId;
    window.location.hash = sectionId;
    
    // Scroll body back to top
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

